package principal.modelErrado;

/**
 *
 * @author artur
 */
public class ProdutoErrado {
    
    
    private final double valor;
    private final String nome;

    public ProdutoErrado(double valor, String nome) {
        this.valor = valor;
        this.nome = nome;
    }

    public double getValor() {
        return valor;
    }

    public String getNome() {
        return nome;
    }
    
    

}
